// ─── nftImage ─────────────────────────────────────────────────────────────────
// fragment CDN: nft.fragment.com/gift/{PascalCase}-{num}.webp
// The name must be PascalCase exactly matching fragment's naming, no spaces/apostrophes.
// Always requires a real instance number — collection-level URLs (no number) return 404.
export function nftImage(slug: string, num?: string): string {
  const displayName = NFT_NAMES[slug] || slug;
  // Remove all non-alphanumeric characters and capitalise each word segment
  const cleanName = displayName
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join('');

  // Prefer passed-in num; fallback to first number in NFT_NUMBERS
  const effectiveNum = (num && num.length > 0)
    ? num
    : (NFT_NUMBERS[slug]?.[0] ?? '1');

  return `https://nft.fragment.com/gift/${cleanName}-${effectiveNum}.webp`;
}

// ─── parseGiftId ──────────────────────────────────────────────────────────────
export function parseGiftId(giftId: string): { slug: string; num: string } {
  if (!giftId) return { slug: '', num: '' };
  const trimmed = giftId.trim().toLowerCase();
  const match   = trimmed.match(/^([a-z]+)-(\d+)$/);
  if (match) return { slug: match[1], num: match[2] };
  return { slug: trimmed, num: '' };
}
